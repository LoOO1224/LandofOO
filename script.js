const root = document.documentElement;
const storedTheme = localStorage.getItem("theme");
const storedLang = localStorage.getItem("lang") || "ko";
const cyclingWords = [
  { label: "Playable", tone: "playable" },
  { label: "Inspired", tone: "inspired" },
  { label: "Chill Out", tone: "chill" }
];
let cyclingWordIndex = 0;

if (storedTheme) {
  root.dataset.theme = storedTheme;
}

function applyLanguage(lang) {
  const nextLang = dictionary[lang] ? lang : "ko";
  root.lang = nextLang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = dictionary[nextLang][key] || dictionary.ko[key] || node.textContent;
  });
  updateCyclingWord(false);
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.lang === nextLang);
  });
  localStorage.setItem("lang", nextLang);
}

function updateCyclingWord(animate = true) {
  const word = cyclingWords[cyclingWordIndex];
  document.querySelectorAll("[data-cycling-word]").forEach((node) => {
    if (animate) {
      node.classList.remove("is-swapping");
      void node.offsetWidth;
      node.classList.add("is-swapping");
    }
    node.textContent = word.label;
    node.dataset.wordTone = word.tone;
  });
}

window.setInterval(() => {
  cyclingWordIndex = (cyclingWordIndex + 1) % cyclingWords.length;
  updateCyclingWord(true);
}, 2400);

document.querySelector("[data-theme-toggle]").addEventListener("click", () => {
  const nextTheme = root.dataset.theme === "dark" ? "light" : "dark";
  root.dataset.theme = nextTheme;
  localStorage.setItem("theme", nextTheme);
});

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelector("[data-copy-email]").addEventListener("click", async (event) => {
  const button = event.currentTarget;
  const lang = localStorage.getItem("lang") || "ko";
  try {
    await navigator.clipboard.writeText("wooyoungcheon@example.com");
    button.textContent = dictionary[lang].copied;
    window.setTimeout(() => {
      button.textContent = dictionary[lang].copyEmail;
    }, 1500);
  } catch {
    window.location.href = "mailto:wooyoungcheon@example.com";
  }
});

applyLanguage(storedLang);

const profileImageUrl = "assets/profile-photo.png?v=28";

fetch(profileImageUrl, { method: "HEAD", cache: "no-store" })
  .then((response) => {
    if (!response.ok) {
      return;
    }

    document.querySelectorAll("[data-profile-image]").forEach((image) => {
      image.src = profileImageUrl;
    });
  })
  .catch(() => {});
